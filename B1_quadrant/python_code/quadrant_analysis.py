# RUN ALL 9 COMBINATIONS
# ─────────────────────────────────────────────
normalisations = ['log', 'linear', 'sqrt']
configs        = [1, 2, 3]
model_labels   = {
    ('log',    1): '1A', ('log',    2): '1B', ('log',    3): '1C',
    ('linear', 1): '2A', ('linear', 2): '2B', ('linear', 3): '2C',
    ('sqrt',   1): '3A', ('sqrt',   2): '3B', ('sqrt',   3): '3C',
}

summary_rows = []
all_results  = {}

print("=" * 70)
print("RUNNING ALL 9 MODEL CONFIGURATIONS")
print("=" * 70)

for norm in normalisations:
    for config in configs:
        label = model_labels[(norm, config)]

        df_result, commit_mean, conv_mean = run_model(df, norm, config)

        # Calculate accuracy — must come before calculate_misses print block
        cinema_acc, streaming_acc, overall_acc = calculate_accuracy(df_result)

        print(f"\nModel {label} | Normalisation: {norm} | Config: {config}")
        print(f"  Commitment mean: {commit_mean:.4f} | Convenience mean: {conv_mean:.4f}")
        print(f"  Cinema accuracy:    {cinema_acc}%")
        print(f"  Streaming accuracy: {streaming_acc}%")
        print(f"  Overall accuracy:   {overall_acc}%")

        # Calculate misses — called here so miss_data exists before summary_rows
        miss_data = calculate_misses(df_result)

        all_results[label] = df_result

        summary_rows.append({
            'Model':                        label,
            'Normalisation':                norm,
            'Config':                       config,
            'Commit mean threshold':        round(commit_mean, 4),
            'Conv mean threshold':          round(conv_mean,   4),
            'Cinema accuracy (%)':          cinema_acc,
            'Streaming accuracy (%)':       streaming_acc,
            'Overall accuracy (%)':         overall_acc,
            'Cinema miss rate (%)':         miss_data['Cinema miss rate (%)'],
            'Streaming miss rate (%)':      miss_data['Streaming miss rate (%)'],
            'False commitment rate (%)':    miss_data['False commitment rate (%)'],
            'False convenience rate (%)':   miss_data['False convenience rate (%)'],
            'Cinema severe misses (S3)':    miss_data['Cinema severe misses (S3)'],
            'Cinema mild misses (S2)':      miss_data['Cinema mild misses (S2)'],
            'Streaming severe misses (S1)': miss_data['Streaming severe misses (S1)'],
            'Streaming mild misses (S2)':   miss_data['Streaming mild misses (S2)'],
        })

        # Cinema crosstab
