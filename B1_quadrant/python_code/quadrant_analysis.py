        # Streaming crosstab
        streaming_df = df_result[df_result['performance'].isin(['Streaming-hit', 'Streaming-flop'])]
        if len(streaming_df) > 0:
            streaming_cross = pd.crosstab(streaming_df['quadrant'], streaming_df['performance'])
            print("\n  Streaming breakdown:")
            print(streaming_cross.to_string())
        else:
            print("\n  Streaming breakdown: No streaming rows found.")

        print("-" * 70)


