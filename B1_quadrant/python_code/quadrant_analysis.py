# ─────────────────────────────────────────────
# HELPER: MPAA AND GENRE MULTIPLIERS
# ─────────────────────────────────────────────
def mpaa_multiplier(val):
    return 1.1 if str(val).strip() in ['PG-13', 'R'] else 1.0

def genre_commitment_mult(val):
    commit_genres = ['Action', 'Adventure', 'Animation']
    return 1.1 if any(g in str(val) for g in commit_genres) else 1.0

def genre_convenience_mult(val):
    conv_genres = ['Drama', 'Romance', 'Comedy']
    return 1.1 if any(g in str(val) for g in conv_genres) else 1.0

# ─────────────────────────────────────────────
# MAIN FUNCTION: CALCULATE INDICES + QUADRANT
# ─────────────────────────────────────────────
def run_model(df_in, normalisation='log', config=1):
    df = df_in.copy()

    df = df.dropna(subset=['numVotes', 'averageRating', 'runTime', 'combined'])
    df = df[df['numVotes'] > 0]
    df = df[df['runTime'] > 0]

    # Normalise numVotes
    max_votes = df['numVotes'].max()
    if normalisation == 'log':
        df['votes_norm'] = np.log10(df['numVotes']) / np.log10(max_votes)
    elif normalisation == 'linear':
        df['votes_norm'] = df['numVotes'] / max_votes
    elif normalisation == 'sqrt':
        df['votes_norm'] = np.sqrt(df['numVotes']) / np.sqrt(max_votes)

    df['runtime_norm'] = df['runTime'].apply(lambda x: min(x, 180) / 180)
    df['rating_norm']  = df['averageRating'] / 10

    df['primary_mult']      = df['combined'].apply(lambda x: 1.5 if str(x).strip().lower() == 'old' else 1.0)
    df['primary_mult_conv'] = df['combined'].apply(lambda x: 1.5 if str(x).strip().lower() == 'new' else 1.0)

    if config == 1:
        df['commitment_logic'] = (
            df['primary_mult'] * df['votes_norm'] * df['runtime_norm']
        )
        df['convenience_logic'] = (
            df['primary_mult_conv'] * df['rating_norm'] * (1 - df['runtime_norm'])
        )

    elif config == 2:
        df['commitment_logic'] = (
            df['primary_mult'] * df['votes_norm'] * df['rating_norm'] * df['runtime_norm']
        )
        df['convenience_logic'] = (
            df['primary_mult_conv'] * df['votes_norm'] * df['rating_norm'] * (1 - df['runtime_norm'])
        )

    elif config == 3:
        df['mpaa_mult']         = df['MPAA_Rating'].apply(mpaa_multiplier)
        df['genre_commit_mult'] = df['Genre'].apply(genre_commitment_mult)
        df['genre_conv_mult']   = df['Genre'].apply(genre_convenience_mult)

        df['commitment_logic'] = (
            df['primary_mult'] * df['votes_norm'] * df['rating_norm']
            * df['runtime_norm'] * df['mpaa_mult'] * df['genre_commit_mult']
        )
        df['convenience_logic'] = (
            df['primary_mult_conv'] * df['votes_norm'] * df['rating_norm']
            * (1 - df['runtime_norm']) * df['genre_conv_mult']
        )

    # Quadrant assignment using MEAN thresholds
    commit_mean = df['commitment_logic'].mean()
    conv_mean   = df['convenience_logic'].mean()

    def assign_quadrant(row):
        high_c  = row['commitment_logic']  >= commit_mean
        high_cv = row['convenience_logic'] >= conv_mean
        if high_c and high_cv:
            return 'Scenario 4: Hybrid Logic'
        elif high_c and not high_cv:
            return 'Scenario 1: Strong Commitment'
        elif not high_c and high_cv:
            return 'Scenario 3: Strong Convenience'
        else:
            return 'Scenario 2: Weak Commitment/Convenience'

    df['quadrant'] = df.apply(assign_quadrant, axis=1)

    return df, commit_mean, conv_mean


