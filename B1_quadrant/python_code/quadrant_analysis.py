# ACCURACY CALCULATION
# ─────────────────────────────────────────────
def calculate_accuracy(df):
    cinema    = df[df['performance'].isin(['cinema-hit', 'cinema-flop'])]
    streaming = df[df['performance'].isin(['Streaming-hit', 'Streaming-flop'])]

    cinema_hits_total    = len(cinema[cinema['performance'] == 'cinema-hit'])
    streaming_hits_total = len(streaming[streaming['performance'] == 'Streaming-hit'])

    cinema_correct = len(cinema[
        (cinema['performance'] == 'cinema-hit') &
        (cinema['quadrant'].isin(['Scenario 1: Strong Commitment',
                                   'Scenario 4: Hybrid Logic']))
    ])
    streaming_correct = len(streaming[
        (streaming['performance'] == 'Streaming-hit') &
        (streaming['quadrant'] == 'Scenario 3: Strong Convenience')
    ])

    cinema_acc    = (cinema_correct / cinema_hits_total * 100) if cinema_hits_total > 0 else 0
    streaming_acc = (streaming_correct / streaming_hits_total * 100) if streaming_hits_total > 0 else 0
    overall_acc   = (
        (cinema_correct + streaming_correct) /
        (cinema_hits_total + streaming_hits_total) * 100
    ) if (cinema_hits_total + streaming_hits_total) > 0 else 0

    return round(cinema_acc, 1), round(streaming_acc, 1), round(overall_acc, 1)


# ─────────────────────────────────────────────
# MISS ANALYSIS
# ─────────────────────────────────────────────
def calculate_misses(df):
    cinema    = df[df['performance'].isin(['cinema-hit', 'cinema-flop'])]
    streaming = df[df['performance'].isin(['Streaming-hit', 'Streaming-flop'])]

    cinema_hits_total     = len(cinema[cinema['performance'] == 'cinema-hit'])
    streaming_hits_total  = len(streaming[streaming['performance'] == 'Streaming-hit'])
    cinema_flops_total    = len(cinema[cinema['performance'] == 'cinema-flop'])
    streaming_flops_total = len(streaming[streaming['performance'] == 'Streaming-flop'])

    # Miss counts
    cinema_correct = len(cinema[
        (cinema['performance'] == 'cinema-hit') &
        (cinema['quadrant'].isin(['Scenario 1: Strong Commitment',
                                   'Scenario 4: Hybrid Logic']))])
    cinema_misses  = cinema_hits_total - cinema_correct

    streaming_correct = len(streaming[
        (streaming['performance'] == 'Streaming-hit') &
        (streaming['quadrant'] == 'Scenario 3: Strong Convenience')])
    streaming_misses  = streaming_hits_total - streaming_correct

    cinema_miss_rate    = round(cinema_misses    / cinema_hits_total    * 100, 1) if cinema_hits_total    > 0 else 0
    streaming_miss_rate = round(streaming_misses / streaming_hits_total * 100, 1) if streaming_hits_total > 0 else 0

    # False commitment: cinema-flops predicted as cinema-suitable
    false_commitment = len(cinema[
        (cinema['performance'] == 'cinema-flop') &
        (cinema['quadrant'].isin(['Scenario 1: Strong Commitment',
                                   'Scenario 4: Hybrid Logic']))])
    false_commitment_rate = round(false_commitment / cinema_flops_total * 100, 1) if cinema_flops_total > 0 else 0

    # False convenience: Streaming-flops predicted as streaming-suitable
    false_convenience = len(streaming[
        (streaming['performance'] == 'Streaming-flop') &
        (streaming['quadrant'] == 'Scenario 3: Strong Convenience')])
    false_convenience_rate = round(false_convenience / streaming_flops_total * 100, 1) if streaming_flops_total > 0 else 0

    # Severity: where did missed cinema-hits land?
    cinema_miss_in_s2 = len(cinema[
        (cinema['performance'] == 'cinema-hit') &
        (cinema['quadrant'] == 'Scenario 2: Weak Commitment/Convenience')])
    cinema_miss_in_s3 = len(cinema[
        (cinema['performance'] == 'cinema-hit') &
        (cinema['quadrant'] == 'Scenario 3: Strong Convenience')])

    # Severity: where did missed Streaming-hits land?
    streaming_miss_in_s1 = len(streaming[
        (streaming['performance'] == 'Streaming-hit') &
        (streaming['quadrant'] == 'Scenario 1: Strong Commitment')])
    streaming_miss_in_s2 = len(streaming[
        (streaming['performance'] == 'Streaming-hit') &
        (streaming['quadrant'] == 'Scenario 2: Weak Commitment/Convenience')])

    print(f"  Cinema miss rate:             {cinema_miss_rate}% ({cinema_misses} of {cinema_hits_total} cinema-hits misclassified)")
    print(f"    - Mild miss (Scenario 2):   {cinema_miss_in_s2} films (uncertain, not wrong platform)")
    print(f"    - Severe miss (Scenario 3): {cinema_miss_in_s3} films (predicted streaming for a cinema hit)")
    print(f"  Streaming miss rate:          {streaming_miss_rate}% ({streaming_misses} of {streaming_hits_total} Streaming-hits misclassified)")
    print(f"    - Severe miss (Scenario 1): {streaming_miss_in_s1} films (predicted cinema for a streaming hit)")
    print(f"    - Mild miss (Scenario 2):   {streaming_miss_in_s2} films (uncertain, not wrong platform)")
    print(f"  False commitment rate:        {false_commitment_rate}% ({false_commitment} of {cinema_flops_total} cinema-flops predicted as cinema-suitable)")
    print(f"  False convenience rate:       {false_convenience_rate}% ({false_convenience} of {streaming_flops_total} Streaming-flops predicted as streaming-suitable)")

    return {
        'Cinema miss rate (%)':         cinema_miss_rate,
        'Streaming miss rate (%)':      streaming_miss_rate,
        'False commitment rate (%)':    false_commitment_rate,
        'False convenience rate (%)':   false_convenience_rate,
        'Cinema severe misses (S3)':    cinema_miss_in_s3,
        'Cinema mild misses (S2)':      cinema_miss_in_s2,
        'Streaming severe misses (S1)': streaming_miss_in_s1,
        'Streaming mild misses (S2)':   streaming_miss_in_s2,
    }


