#preparing the Netflix Top 10 data
weeks_count = netflix_trending.groupby('show_title')['cumulative_weeks_in_top_10'].max().reset_index()
weeks_count.columns = ['Title', 'total_weeks']

#Merging weeks_count into the main 'data' dataframe
data = data.merge(weeks_count, on='Title', how='left')

#Setting 'total_weeks' to 0 for anyone NOT 'Streaming' OR where no match was found
data.loc[data['distributor'] != 'Streaming', 'total_weeks'] = 0
data['total_weeks'] = data['total_weeks'].fillna(0)

#Defining a single function that handles BOTH Cinema and Streaming logic
def classify_movie_success(row):
    #STREAMING LOGIC (Streaming)
    if row['distributor'] == 'Streaming':
        weeks = row['total_weeks']
        if weeks > 4:
            return 'Streaming-hit'
        else:
            return 'Streaming-flop'

    #CINEMA LOGIC (Warner Bros)
    else:
        #Checking if it has budget and revenue data
        if row['budget'] > 0 and not np.isnan(row['revenue']):
            roi = row['revenue'] / row['budget']
            if roi >= 3:
                return 'Cinema-hit'
            else:
                return 'Cinema-flop'
        else:
            # Handle cases with no budget/revenue info
            return 'unknown'

#Creating the final 'performance' column
data['performance'] = data.apply(classify_movie_success, axis=1)

